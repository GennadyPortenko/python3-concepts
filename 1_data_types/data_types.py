# boolean
print(type(True))  # <class 'bool'>

# numeric
print(type(1))  # <class 'int'>
print(type(1.1))  # <class 'float'>
print(type(1 + 2j))  # <class 'complex'>

# sequences
print(type("abc"))  # <class 'str'>  -  Immutable
print(type([3, 4, 5]))  # <class 'list'>  -  mutable
print(type((1, 2, 3)))  # <class 'tuple'>  -  Immutable 'list'

# dictionary
print(type({1:'First', 2.0:'Second'}))  # <class 'dictionary'>  -  mutable

